
# multiplication function
def multiply(x,y):
    return x * y

# division function
def divide(x,y):
    return x / y

# addition function
def add(x,y):
    return x + y

# subtraction function
def subtract(x,y):
    return x - y

print("This calculator function will multiply 8 and 9")
x = multiply(8,9)
print(x)

print("This calculator function will divide 8 and 9")
x = divide(8,9)
print(x)

print("This calculator function will add 8 and 9")
x = add(8,9)
print(x)

print("This calculator function will subtract 9 from 8")
x = subtract(8,9)
print(x)

